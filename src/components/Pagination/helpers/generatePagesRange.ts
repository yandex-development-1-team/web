export const generatePagesRange = (currentPage: number, totalPages: number) => {
  if (totalPages <= 7) {
    return Array.from({ length: totalPages }, (_, i) => i + 1)
  }

  const pages: (number | string)[] = [1]

  // 2. ОПРЕДЕЛЕНИЕ ДИНАМИЧЕСКОГО ОКНА
  // По умолчанию мы хотим видеть текущую страницу и её соседей (+-1).
  // Math.max/min гарантируют, что мы не вылезем за 2 и предпоследнюю страницу.
  let start = Math.max(2, currentPage - 1)
  let end = Math.min(totalPages - 1, currentPage + 1)

  // 3. РАСШИРЕНИЕ ОКНА ДЛЯ ПЛАВНОСТИ (СИММЕТРИЯ)
  // Если мы находимся в начале списка (страницы 1, 2, 3, 4):
  if (currentPage <= 4) {
    // Чтобы пагинация не выглядела обрывистой, принудительно показываем всё до 5 страницы.
    // Это избавляет от ситуации "1 ... 3 4 5", превращая её в "1 2 3 4 5".
    end = 5
  }

  // Если мы находимся в самом конце списка:
  if (currentPage >= totalPages - 3) {
    // Принудительно расширяем левую границу окна до (Последняя - 4).
    // Это гарантирует, что в конце всегда будет блок из 5 последних чисел.
    start = totalPages - 4
  }

  // 4. ДОБАВЛЕНИЕ ЛЕВОГО МНОГОТОЧИЯ
  // Если наш расчетный старт (например, 4) больше 2, значит между "1" и "4"
  // есть пропущенные числа. Ставим многоточие.
  if (start > 2) {
    pages.push('...')
  }

  // 5. ЗАПОЛНЕНИЕ ЧИСЛАМИ
  // Проходим циклом от вычисленного старта до конца и добавляем их в массив.
  for (let i = start; i <= end; i++) {
    pages.push(i)
  }

  // 6. ДОБАВЛЕНИЕ ПРАВОГО МНОГОТОЧИЯ
  // Если наш расчетный конец (например, 7) меньше предпоследней страницы (например, 9),
  // значит между ними есть дырка. Ставим многоточие.
  if (end < totalPages - 1) {
    pages.push('...')
  }

  // 7. ЗАВЕРШЕНИЕ
  // Добавляем последнюю страницу, которая всегда должна быть в списке.
  pages.push(totalPages)

  return pages
}
